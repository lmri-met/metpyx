# Fixtures

@pytest.fixture(scope="module")
def quality():
    return Quality("N60")


@pytest.fixture(scope="module")
def spek():
    s = sp.Spek(kvp=60)
    s.multi_filter([["Al", 4.0], ["Cu", 0.6]])
